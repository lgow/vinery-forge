package satisfyu.vinery.client.gui.handler;

import net.minecraft.world.Container;
import net.minecraft.world.SimpleContainer;
import net.minecraft.world.entity.player.Inventory;
import net.minecraft.world.entity.player.Player;
import net.minecraft.world.inventory.*;
import net.minecraft.world.item.ItemStack;
import satisfyu.vinery.registry.VineryScreenHandlerTypes;

public class ApplePressMenu extends AbstractContainerMenu {
	private final Container inventory;

	private final ContainerData propertyDelegate;

	public ApplePressMenu(int syncId, Inventory inventory) {
		this(syncId, inventory, new SimpleContainer(2), new SimpleContainerData(2));
	}

	public ApplePressMenu(int syncId, Inventory playerInventory, Container inventory, ContainerData delegate) {
		super(VineryScreenHandlerTypes.APPLE_PRESS_SCREEN_HANDLER.get(), syncId);
		checkContainerSize(inventory, 2);
		this.inventory = inventory;
		inventory.startOpen(playerInventory.player);
		this.propertyDelegate = delegate;
		this.addSlot(new Slot(inventory, 0, 48, 34));
		this.addSlot(new FurnaceResultSlot(playerInventory.player, inventory, 1, 116, 35));
		addPlayerInventory(playerInventory);
		addPlayerHotbar(playerInventory);
		addDataSlots(delegate);
	}

	public boolean isCrafting() {
		return propertyDelegate.get(0) > 0;
	}

	public int getScaledProgress() {
		int progress = this.propertyDelegate.get(0);
		int maxProgress = this.propertyDelegate.get(1);  // Max Progress
		int progressArrowSize = 26; // This is the width in pixels of your arrow
		return maxProgress != 0 && progress != 0 ? progress * progressArrowSize / maxProgress : 0;
	}

	@Override
	public ItemStack quickMoveStack(Player player, int invSlot) {
		ItemStack newStack = ItemStack.EMPTY;
		Slot slot = this.slots.get(invSlot);
		if (slot != null && slot.hasItem()) {
			ItemStack originalStack = slot.getItem();
			newStack = originalStack.copy();
			if (invSlot < this.inventory.getContainerSize()) {
				if (!this.moveItemStackTo(originalStack, this.inventory.getContainerSize(), this.slots.size(), true)) {
					return ItemStack.EMPTY;
				}
			}
			else if (!this.moveItemStackTo(originalStack, 0, this.inventory.getContainerSize(), false)) {
				return ItemStack.EMPTY;
			}
			if (originalStack.isEmpty()) {
				slot.set(ItemStack.EMPTY);
			}
			else {
				slot.setChanged();
			}
		}
		return newStack;
	}

	@Override
	public boolean stillValid(Player player) {
		return this.inventory.stillValid(player);
	}

	private void addPlayerInventory(Inventory playerInventory) {
		for (int i = 0; i < 3; ++i) {
			for (int j = 0; j < 9; ++j) {
				this.addSlot(new Slot(playerInventory, j + i * 9 + 9, 8 + j * 18, 84 + i * 18));
			}
		}
	}

	private void addPlayerHotbar(Inventory playerInventory) {
		for (int i = 0; i < 9; ++i) {
			this.addSlot(new Slot(playerInventory, i, 8 + i * 18, 142));
		}
	}
}